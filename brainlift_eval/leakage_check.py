"""Leakage check: scan the SERVED (post-gate) set of generated analogs against the
gold/test set and flag TRUE near-duplicates. A genuine analog re-parameterizes the
source: same concept and phrasing is expected, so phrasing similarity alone is NOT
leakage. Leakage is when a student who saw a gold/test item gets the analog for
free — i.e. the generated question is near-verbatim AND resolves to the SAME
correct answer.

The generation pipeline runs a leakage GATE on every item before serving it:
near-verbatim + same-answer items are REGENERATED (up to MAX_REGEN with a stronger
re-parameterize instruction) and, if still leaking, BLOCKED (withheld). This check
scans what is actually SERVED, so a clean shipped pipeline reports CLEAN.

We stay HONEST: the leakage definition is unchanged, and we report how many raw
items leaked, how many were caught-and-regenerated, and how many were blocked, so
it is transparent that the raw model occasionally leaks but the pipeline removes it
before students see anything. We report the worst phrasing overlap for
transparency, but flag an item only when question overlap >= QUESTION_SIM AND the
generated correct answer equals the gold answer.
"""

from __future__ import annotations

import sys

from _common import blai, generate_served_for_gold, get_generator, jaccard
from gold_set import GOLD

QUESTION_SIM = 0.90  # near-verbatim question wording (matches the generation gate)


def _generated_answer(a) -> str:
    return a.choices[a.correct_index]


def run(live: bool = False) -> bool:
    client, label = get_generator(live)
    gated, stats = generate_served_for_gold(GOLD, client)

    # Only SERVED items are put in front of students; blocked items are withheld.
    served = [(g.analog, src) for g, src in zip(gated, GOLD) if g.served]

    flagged = []
    worst_phrasing = 0.0
    for a, src in served:
        gen_ans = _generated_answer(a).strip().lower()
        for g in GOLD:
            sim = jaccard(a.question, g["front"])
            worst_phrasing = max(worst_phrasing, sim)
            # true leak: near-verbatim wording AND identical answer
            if sim >= QUESTION_SIM and gen_ans == g["back"].strip().lower():
                flagged.append((src["id"], g["id"], round(sim, 3)))
                break

    blocked_ids = [src["id"] for g, src in zip(gated, GOLD) if g.blocked]

    print("== Leakage check (SERVED / post-gate set) ==")
    print(f"generator: {label}")
    print(f"raw items generated: {stats['total']}")
    print(f"raw items that leaked (near-verbatim + same answer): {stats['leaked_raw']}")
    print(f"caught & regenerated by gate (now clean): {stats['caught_regenerated']}")
    print(f"blocked/withheld after {blai.MAX_REGEN} retries: {stats['blocked']} {blocked_ids}")
    print(f"served items scanned: {len(served)}")
    print(f"question-similarity threshold: {QUESTION_SIM} (plus identical answer)")
    print(f"worst phrasing overlap (expected high; same concept): {worst_phrasing:.3f}")
    print(f"flagged true duplicates in SERVED set: {len(flagged)} {flagged}")
    clean = len(flagged) == 0
    print(
        f"RESULT: {'CLEAN (served set has zero leaked items)' if clean else 'LEAKAGE DETECTED IN SERVED SET'}"
    )
    return clean


if __name__ == "__main__":
    ok = run(live="--live" in sys.argv)
    sys.exit(0 if ok else 1)
