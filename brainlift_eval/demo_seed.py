"""Generate SIMULATED demo collections for every BrainLift score profile.

WHY THIS EXISTS
---------------
The three BrainLift scores (Memory / Performance / Readiness) only show a number
once there is enough *real* data behind them — Readiness in particular abstains
until there are >= 200 graded reviews, >= 50% syllabus coverage AND a completed
diagnostic (the honest give-up rule in ``anki.brainlift.measurements``). A fresh
demo collection therefore shows "Not enough data", which is useless for a demo
video.

This script builds a throwaway collection **per accuracy profile**, tags cards
across the whole Exam P syllabus, drives real FSRS reviews at the profile's
target accuracy, and saves a matching diagnostic — so opening the exported deck
shows genuine, *calculated* Memory / Performance / Readiness numbers (including
the headline ~90%-accuracy profile whose Readiness clears the give-up gate).

HONESTY NOTE
------------
This is **SIMULATED** data: the reviews and diagnostic responses are generated by
a seeded RNG to a target accuracy, not recorded from a real student. The numbers
the dashboard then computes are real (they run the shipped Rust ``topic_mastery``
engine + the shipped measurement formulas over this simulated history), but they
describe a *simulated* learner, not field data.

Run (one command, from the anki/ dir):

    out/pyenv/bin/python brainlift_eval/demo_seed.py

Options:  --profile NAME  (strong|medium|weak; default: all)
          --cards N       (default 260)   --reviews N (default 260)
          --out DIR       (default ../dist/demo-profiles)

Each profile writes ``<out>/brainlift-demo-<profile>.colpkg`` (import via
File > Import) and prints the computed scores.
"""

from __future__ import annotations

import os
import random
import sys
import time
import zlib

_HERE = os.path.dirname(os.path.abspath(__file__))
# Use the BUILT pylib (needs the compiled Rust backend to open a Collection).
sys.path.insert(0, os.path.join(_HERE, "..", "out", "pylib"))

from anki.collection import AddNoteRequest, Collection  # noqa: E402
from anki.brainlift import diagnostic as dx  # noqa: E402
from anki.brainlift import exam_p  # noqa: E402
from anki.brainlift import measurements as mx  # noqa: E402

# --- accuracy profiles ------------------------------------------------------
# ``accuracy`` drives both the fraction of reviews answered "Good/Easy" (vs
# "Again") and the diagnostic score. ``confidence`` seeds the diagnostic's
# self-rated confidence. Ranges deliberately span strong -> weak so a demo can
# show three visibly different-but-real readiness numbers.
PROFILES: dict[str, dict[str, float]] = {
    "strong": {"accuracy": 0.90, "confidence": dx.CONFIDENCE_HIGH},
    "medium": {"accuracy": 0.70, "confidence": dx.CONFIDENCE_MEDIUM},
    "weak": {"accuracy": 0.50, "confidence": dx.CONFIDENCE_LOW},
}

DEFAULT_CARDS = 260
DEFAULT_REVIEWS = 260
BATCH = 200


def _all_subtopic_tags() -> list[str]:
    tags: list[str] = []
    for topic in exam_p.SYLLABUS:
        for sub in topic.subtopics:
            tags.append(sub.tag(topic.key))
    return tags


def _build_deck(col: Collection, n_cards: int) -> None:
    """Add ``n_cards`` Basic notes tagged across EVERY Exam P subtopic so the
    syllabus-coverage search finds cards in all three main topics (100% cover)."""
    basic = col.models.by_name("Basic")
    assert basic is not None, "Basic notetype missing"
    deck_id = col.decks.id("Exam P — Sample Questions")
    tags = _all_subtopic_tags()
    made = 0
    while made < n_cards:
        batch = min(BATCH, n_cards - made)
        reqs = []
        for k in range(batch):
            idx = made + k
            note = col.new_note(basic)
            note["Front"] = f"Exam P practice {idx}: P(A|B) for scenario {idx}?"
            note["Back"] = f"Worked solution {idx}."
            note.tags = [tags[idx % len(tags)]]
            reqs.append(AddNoteRequest(note=note, deck_id=deck_id))
        col.add_notes(reqs)
        made += batch
    col.decks.set_current(deck_id)
    conf = col.decks.config_dict_for_deck_id(deck_id)
    conf["new"]["perDay"] = max(conf["new"]["perDay"], n_cards)
    conf["rev"]["perDay"] = max(conf["rev"]["perDay"], n_cards)
    col.decks.update_config(conf)


def _review(col: Collection, n_reviews: int, accuracy: float, rng: random.Random) -> int:
    """Answer up to ``n_reviews`` cards, choosing the button by ``accuracy`` so a
    strong profile builds high FSRS retrievability and a weak one lapses more."""
    done = 0
    for _ in range(n_reviews):
        card = col.sched.getCard()
        if card is None:
            break
        if rng.random() < accuracy:
            rating = 4 if rng.random() < 0.35 else 3  # Easy / Good
        else:
            rating = 1 if rng.random() < 0.6 else 2  # Again / Hard
        col.sched.answerCard(card, rating)
        done += 1
    return done


def _age_reviews(col: Collection, rng: random.Random, lo_days: int, hi_days: int) -> int:
    """Backdate each reviewed card's last review into the past.

    FSRS retrievability decays with time since the last review, so a collection
    reviewed "just now" reports ~100% Memory for every profile. Pushing the last
    review a few days back lets the *stability* each profile actually earned
    (high for Good/Easy reviews, low for Again/Hard) drive a realistic,
    profile-dependent Memory number instead of a flat 100%."""
    now = int(time.time())
    aged = 0
    cards = []
    for cid in col.find_cards(""):
        card = col.get_card(cid)
        if card.reps > 0 and card.memory_state is not None:
            card.last_review_time = now - rng.randint(lo_days, hi_days) * 86400
            cards.append(card)
            aged += 1
    if cards:
        col.update_cards(cards)
    return aged


def _save_diagnostic(col: Collection, accuracy: float, confidence: float) -> None:
    """Persist a diagnostic result at the target accuracy over the fixed bank."""
    bank = dx.QUESTION_BANK
    n_correct = round(accuracy * len(bank))
    responses: list[dx.DiagnosticResponse] = []
    for i, q in enumerate(bank):
        correct = i < n_correct
        chosen = q.correct_index if correct else (q.correct_index + 1) % len(q.choices)
        responses.append(
            dx.DiagnosticResponse(
                question_id=q.id,
                chosen_index=chosen,
                time_seconds=45.0,
                confidence=confidence,
            )
        )
    dx.run_diagnostic(col, responses)


def _compute(col: Collection) -> dict:
    coverage = exam_p.coverage_report(col)
    memory = mx.compute_memory(coverage)
    perf = mx.compute_performance(col)
    total_reviews = sum(t.total_reviews for t in coverage.topics)
    readiness = mx.compute_readiness(col, coverage, memory, perf, total_reviews)
    return {
        "coverage_percent": round(coverage.coverage_percent, 1),
        "studied_coverage_percent": round(coverage.studied_coverage_percent, 1),
        "total_reviews": total_reviews,
        "memory": memory,
        "performance": perf,
        "readiness": readiness,
    }


def _print_profile(name: str, r: dict) -> None:
    m, p, rd = r["memory"], r["performance"], r["readiness"]
    print(f"\n=== profile: {name} ===")
    print(f"  syllabus coverage : {r['coverage_percent']:.0f}%  "
          f"(studied {r['studied_coverage_percent']:.0f}%)   "
          f"graded reviews: {r['total_reviews']}")
    if m.available:
        print(f"  Memory      : {m.point*100:.0f}%  "
              f"[{m.low*100:.0f}-{m.high*100:.0f}%]  conf={m.confidence_level}  "
              f"({m.reviewed_cards} reviewed cards)")
    else:
        print("  Memory      : not available")
    if p.available:
        print(f"  Performance : {p.point*100:.0f}%  "
              f"[{p.low*100:.0f}-{p.high*100:.0f}%]  conf={p.confidence_level}  "
              f"({p.answered} diagnostic Qs)")
    else:
        print("  Performance : not available")
    if rd.available:
        print(f"  Readiness   : {rd.projected_score}/10  "
              f"[{rd.score_low}-{rd.score_high}]  "
              f"pass-prob={rd.pass_probability}  conf={rd.confidence_level}  "
              "*** SHOWS A NUMBER ***")
    else:
        print(f"  Readiness   : ABSTAINS -> {rd.missing_evidence}")


def seed_profile(
    name: str, out_dir: str, n_cards: int, n_reviews: int
) -> dict:
    spec = PROFILES[name]
    accuracy = float(spec["accuracy"])
    confidence = float(spec["confidence"])
    # Deterministic seed: use a stable checksum (not Python's per-process salted
    # hash()) so the demo numbers are reproducible run-to-run and match the
    # numbers documented in BRAINLIFT_EDITIONS.md section 7.
    rng = random.Random(zlib.crc32(f"brainlift-demo:{name}".encode()))

    os.makedirs(out_dir, exist_ok=True)
    anki2 = os.path.join(out_dir, f"brainlift-demo-{name}.anki2")
    colpkg = os.path.join(out_dir, f"brainlift-demo-{name}.colpkg")
    for stale in (anki2, colpkg):
        if os.path.exists(stale):
            os.remove(stale)

    col = Collection(anki2)
    col.set_config("fsrs", True)  # so reviewing populates FSRS retrievability
    # mark BrainLift onboarding/diagnostic done so the app treats it as a live user
    col.set_config("brainlift_seeded_default", True)
    print(f"[{name}] building {n_cards} tagged cards ...", flush=True)
    _build_deck(col, n_cards)
    reviewed = _review(col, n_reviews, accuracy, rng)
    aged = _age_reviews(col, rng, lo_days=3, hi_days=14)
    print(f"[{name}] reviewed {reviewed} cards @ target accuracy {accuracy:.0%} "
          f"(aged {aged} cards 3-14 days so FSRS recall decays realistically)", flush=True)
    _save_diagnostic(col, accuracy, confidence)

    result = _compute(col)
    _print_profile(name, result)

    # export a loadable package LAST (this closes the collection for full sync)
    col.export_collection_package(colpkg, include_media=False, legacy=True)
    print(f"[{name}] wrote {colpkg}")
    return result


def _parse_args(argv: list[str]) -> tuple[list[str], str, int, int]:
    profiles = list(PROFILES.keys())
    out_dir = os.path.join(_HERE, "..", "..", "dist", "demo-profiles")
    cards, reviews = DEFAULT_CARDS, DEFAULT_REVIEWS
    for i, a in enumerate(argv):
        if a == "--profile" and i + 1 < len(argv):
            profiles = [argv[i + 1]]
        elif a == "--out" and i + 1 < len(argv):
            out_dir = argv[i + 1]
        elif a == "--cards" and i + 1 < len(argv):
            cards = int(argv[i + 1])
        elif a == "--reviews" and i + 1 < len(argv):
            reviews = int(argv[i + 1])
    return profiles, os.path.abspath(out_dir), cards, reviews


def main(argv: list[str]) -> int:
    profiles, out_dir, cards, reviews = _parse_args(argv)
    print("== BrainLift demo-profile seeder (SIMULATED data) ==")
    print(f"out: {out_dir}")
    for name in profiles:
        if name not in PROFILES:
            print(f"unknown profile: {name} (choose from {list(PROFILES)})")
            return 2
        seed_profile(name, out_dir, cards, reviews)
    print("\nDone. Import a .colpkg via File > Import to demo real, calculated scores.")
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
